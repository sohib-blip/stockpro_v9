function translateDynamic(locale: Exclude<Locale, "en">, value: string) {
  const rules =
    locale === "fr"
      ? [
          [/^Page (\d+)$/, "Page $1"],
          [/^Label #(\d+)$/, "Étiquette n°$1"],
          [/^Users \((\d+)\)$/, "Utilisateurs ($1)"],
          [/^Last (\d+) activities$/, "$1 dernières activités"],
          [/^Detected: (\d+)$/, "$1 détectés"],
          [/^(\d+) detected$/, "$1 détectés"],
          [/^(\d+) low · (\d+) empty — see tables below$/, "$1 faibles · $2 vides — voir les tableaux ci-dessous"],
          [/^\+ (\d+) more device bins · click a row for box and floor detail$/, "+ $1 autres emplacements · cliquez sur une ligne pour les détails"],
          [/^(\d+) device bins · click a row for box and floor detail$/, "$1 emplacements · cliquez sur une ligne pour les détails"],
          [/^\+ (\d+) more accessories$/, "+ $1 autres accessoires"],
          [/^(\d+) accessories$/, "$1 accessoires"],
          [/^Download all labels — PDF \((\d+)\)$/, "Télécharger toutes les étiquettes — PDF ($1)"],
          [/^Device (.+)$/, "Appareil $1"],
          [/^Floor (.+)$/, "Étage $1"],
        ]
      : [
          [/^Page (\d+)$/, "Pagina $1"],
          [/^Label #(\d+)$/, "Label $1"],
          [/^Users \((\d+)\)$/, "Gebruikers ($1)"],
          [/^Last (\d+) activities$/, "Laatste $1 activiteiten"],
          [/^Detected: (\d+)$/, "$1 gedetecteerd"],
          [/^(\d+) detected$/, "$1 gedetecteerd"],
          [/^(\d+) low · (\d+) empty — see tables below$/, "$1 laag · $2 leeg — zie de tabellen hieronder"],
          [/^\+ (\d+) more device bins · click a row for box and floor detail$/, "+ $1 andere apparaatvakken · klik op een rij voor details"],
          [/^(\d+) device bins · click a row for box and floor detail$/, "$1 apparaatvakken · klik op een rij voor details"],
          [/^\+ (\d+) more accessories$/, "+ $1 andere accessoires"],
          [/^(\d+) accessories$/, "$1 accessoires"],
          [/^Download all labels — PDF \((\d+)\)$/, "Alle labels downloaden — PDF ($1)"],
          [/^Device (.+)$/, "Apparaat $1"],
          [/^Floor (.+)$/, "Verdieping $1"],
        ];

  for (const [pattern, replacement] of rules as Array<[RegExp, string]>) {
    if (pattern.test(value)) return value.replace(pattern, replacement);
  }
  return value;
}

export function translateUiText(locale: Locale, source: string) {
  if (locale === "en" || !source) return source;
  const leading = source.match(/^\s*/)?.[0] ?? "";
  const trailing = source.match(/\s*$/)?.[0] ?? "";
  const value = source.trim();
  if (!value) return source;
  const translated = DICTIONARIES[locale][value] ?? translateDynamic(locale, value);
  return `${leading}${translated}${trailing}`;
}
